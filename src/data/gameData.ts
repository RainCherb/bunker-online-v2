export function calculateBunkerSlots(playerCount: number): number {
  return Math.floor(playerCount / 2);
}

export function getCharacteristicsToReveal(round: number, playerCount: number): number {
  if (playerCount <= 6) {
    if (round === 1) return 3;
    if (round === 2) return 3;
    if (round === 3) return 2;
    return 0;
  }
  if (playerCount <= 8) {
    if (round === 1) return 3;
    if (round === 2) return 2;
    if (round === 3) return 2;
    return 1;
  }
  if (playerCount <= 10) {
    if (round === 1) return 3;
    if (round === 2) return 2;
    if (round === 3) return 1;
    return 1;
  }
  if (playerCount <= 12) {
    if (round === 1) return 2;
    if (round === 2) return 2;
    if (round === 3) return 1;
    return 1;
  }
  // 13-15 players
  if (round === 1) return 2;
  if (round === 2) return 1;
  return 1;
}
